class Calculator() {  //deinisi class Calculator
    fun tambah(a: Int, b: Int): Int { //create function tambah with 2 params
        return a + b //return varaibel a sum b taken parameter
    }

    fun kurang(a: Int, b: Int): Int { //create function kurang with 2 param
        return a - b //return varaibel a subtraction b taken parameter
    }

    fun kali(a: Int, b: Int): Int {//create function kali with 2 param
        return a * b //return varaibel a multiple b taken parameter
    }

    fun bagi(a: Int, b: Int): Int { //create function bagi with 2 param
        return a / b //create function bagi with 2 param
    }
}